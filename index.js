let main_div1 =document.querySelector("#main_div1")
let icon_per =document.querySelector("#icon_per")
let person_details =document.querySelector("#person_details")

main_div1.onmouseover =()=>{
    icon_per.style.display="block"
    person_details.style.backgroundColor ="rgb(58,69,77)"
    person_details.style.color ="white"
}
main_div1.onmouseout =()=>{
    icon_per.style.display="none"
    person_details.style.backgroundColor ="#c8cbcf"
    person_details.style.color ="black"
}
